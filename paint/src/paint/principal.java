    public LookupTable seno(double w){
 
        double K = 255.0;
        byte lt[] = new byte[256];
        lt[0]=0;
        for (int i=1; i<256; ++i){
            lt[i] = (byte)(K*(Math.sin(w*i)));
            //if(lt[i]<0)
                //lt[i]*=-1.0;
        }
        ByteLookupTable slt = new ByteLookupTable(0,lt);
        return slt;
    }       
    
    

